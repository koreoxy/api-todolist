import mongoose from 'mongoose';

// SCHEMA
const User = mongoose.Schema(
  {
    // FIELD
    name: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

export default mongoose.model('Users', User);
